# if, else

print("Can you drink?")
age = int(input("How old are you? ")) #Below is comparing to an integer. int() is required.

if age >= 21: #if the variable input for age is equal to or greater than 21
    print("You're old enough! Go have a drink!") #then this
else: #if the variable input for age is not equal to or greater than 21
    print("You're too young. Don't get caught.") # then this

# the inforation after if is the 'condition'
#the indentation folling the if and else are 'blocked code'
    
